                # –ü–æ—Å–ª–µ–¥–Ω—è—è "–Ω–µ–ø–æ–ª–Ω–∞—è" —Å—Ç—Ä–æ–∫–∞
                list_str.append(value[lower_cut:])
                # –†–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å (–æ—Ç—Å—Ç—É–ø)
                space = '\n' + ' ' * count_space
